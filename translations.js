// Function to get translation with fallback to French
function t(key, lang = 'fr') {
  // Fallback to French if language not supported
  if (!translations[lang]) {
    lang = 'fr';
  }
  
  const keys = key.split('.');
  let value = translations[lang];
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  // If translation not found, try French as fallback
  if (!value && lang !== 'fr') {
    value = translations['fr'];
    for (const k of keys) {
      value = value?.[k];
    }
  }
  
  // If still not found, return the key itself (never return undefined or empty)
  return value || (lang === 'fr' ? key : t(key, 'fr')) || key;
}

// Function to replace placeholders in messages
function replacePlaceholders(text, replacements) {
  let result = text;
  for (const [key, value] of Object.entries(replacements)) {
    result = result.replace(`{${key}}`, value);
  }
  return result;
}

